package timezone

import (
	"time"
)

type (
	// offsetKey - ключ индекса подбора часового пояса: смещение
	// относительно UTC в секундах и признак летнего времени.
	offsetKey struct {
		offset int
		isDST  bool
	}
)

// NameByOffset - подбирает имя зарегистрированного часового пояса по указанному
// смещению относительно UTC и признаку летнего времени. Сообщает false, если
// подходящего пояса нет. Часовой пояс процесса ("Local") не подбирается никогда,
// так как вызывающему требуется IANA-имя.
//
// Если пара (смещение, признак летнего времени) подходит нескольким поясам
// (например, Europe/Berlin и Europe/Paris зимой), то возвращается тот из них,
// который был указан в списке последним. Исключение - UTC: он регистрируется
// после всего списка, поэтому пару (+00:00, isDST=false) всегда получает он,
// даже если её занимает пояс из списка (например, Atlantic/Reykjavik).
//
// ВАЖНО: индекс строится один раз при создании списка и на год вперёд от этого
// момента, поэтому изменение правил перехода на летнее время в новой версии базы
// часовых поясов будет учтено только после перезапуска приложения.
func (l *LocationList) NameByOffset(offset time.Duration, isDST bool) (string, bool) {
	name, ok := l.offsetIndex[offsetKey{offset: int(offset / time.Second), isDST: isDST}]

	return name, ok
}

// addToOffsetIndex - регистрирует пояс в индексе по всем состояниям, в которых
// он пребывает в течение года, следующего за моментом now. Все состояния
// кладутся сразу, поэтому результат подбора не зависит от того, в какое время
// года выполняется запрос.
//
// Момент now передаётся извне, чтобы все пояса одного списка индексировались
// по одному и тому же окну, а не каждый по своему. Окно отсчитывается от дня now,
// а не от начала календарного года: у сервиса, запущенного в конце декабря,
// календарный индекс сохранял бы отменённые в новом году состояния и не получал бы
// новые (так было у Asia/Tehran на рубеже 2022/2023).
//
// Окно сканируется посуточно, а не зондируется в характерных точках, так как
// состояние пояса не обязано совпадать с сезоном, а признак летнего времени
// не всегда означает "лето" (Africa/Casablanca круглый год +01 с признаком
// летнего времени и переходом на +00 в месяц Рамадана), поэтому короткие
// и плавающие по календарю состояния зондированием терялись бы. По той же
// причине нигде не предполагается величина сдвига: часу она равна не у всех
// поясов (у Australia/Lord_Howe - 30 минут).
func addToOffsetIndex(index map[offsetKey]string, name string, loc *time.Location, now time.Time) {
	local := now.In(loc)

	// сутки берутся по полудню, чтобы проба не приходилась на несуществующий
	// или неоднозначный час: переходы выполняются ночью
	start := time.Date(local.Year(), local.Month(), local.Day(), 12, 0, 0, 0, loc)
	end := start.AddDate(1, 0, 0)

	for day := start; day.Before(end); day = day.AddDate(0, 0, 1) {
		_, offset := day.Zone()

		index[offsetKey{offset: offset, isDST: day.IsDST()}] = name
	}
}
